// Uso de 'LET'

// Declarar e inicializar una variable
let nombre="uriel"
console.log(nombre)

// Declarar y luego inicializar una variable
let apellido
apellido = "martinez"
console.log(apellido)

// Declarar,inicializar una variable y volver a asignar un valor
let numero = 3
numero = "hola"
console.log(numero)